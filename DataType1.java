package DataType;

public class DataType1 {
    public static void main(String[] args) {
        System.out.println(6);//Number
        System.out.println("six");//String

        System.out.println("6");//String 6 사람이 보기엔 숫자지만 컴퓨터가 보기에는 문자이다.
        System.out.println(6+6);
        System.out.println("6"+"6");//여기서 '+'는 더하기 위한 것이 아닌 연결시키는 결합연산자

        System.out.println(6*6);
        // System.out.println("6"*"6");

        System.out.println("1111".length());//length()는 문자열의 길이를 알려줌
        // System.out.println(1111.length());//숫자는 그 숫자의 길이를 알려주는 연산은 없다. 

         
    }
}

